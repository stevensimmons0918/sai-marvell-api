extern  char*  CNFG_chck_str(
    /*!     INPUTS:             */
      char    *str
)
{
/*!*************************************************************************/
/*! L O C A L   D E C L A R A T I O N S   A N D   I N I T I A L I Z A T I O N */
/*!*************************************************************************/

  char     *p, *s;

/*!*************************************************************************/
/*!                      F U N C T I O N   L O G I C                          */
/*!*************************************************************************/

  if( str)
   {
     p = str;
     while( isspace(*p)) p++;
     
     if( *p != '\0')
      {
        strlwr( str);
        if( (s = strrchr( p, '\n')) != NULL) *s = '\0';
        if( ((' ' <= *p) && (*p <= '~')) &&  *p != ';')  {
            //cut the tail of spaces
            for(s = &p[strlen(p) - 1] ; s != p && isspace(*s) ; s--)
                ;
            *(s+1) = '\0' ;
            return  p;
        }
      }
   }

  return NULL;
}
