/*-----------------------------------------------------------
 * Implementation of functions defined in portable.h for the ARM7 port.
 *
 * Components that can be compiled to either ARM or THUMB mode are
 * contained in this file.  The ISR routines, which can only be compiled
 * to ARM mode are contained in portISR.c.
 *----------------------------------------------------------*/

/* Scheduler includes. */
#include "FreeRTOS.h"
#include "task.h"
#include <cntmr.h>
#include <ic.h>
#include <clock.h>

/* Constants required to setup the task context. */
#define portINITIAL_SPSR				( ( portSTACK_TYPE ) 0x1f ) /* System mode, ARM mode, interrupts enabled. */
#define portTHUMB_MODE_BIT				( ( portSTACK_TYPE ) 0x20 )
#define portINSTRUCTION_SIZE			( ( portSTACK_TYPE ) 4 )
#define portNO_CRITICAL_SECTION_NESTING	( ( portSTACK_TYPE ) 0 )

/* Clocks */
unsigned long ulCpuClk = MV_CPU_CLK_DEFAULT;
unsigned long ulTClk;
unsigned long ulL2Clk;

/* Scheduler */
static long lSchedStarted = 0;
portBASE_TYPE xPortStartScheduler( void );

/* Setup the timer to generate the tick interrupts. */
static void prvSetupTimerInterrupt( void );

/* 
 * The scheduler can only be started from ARM mode, so 
 * vPortISRStartFirstSTask() is defined in portISR.c. 
 */
extern void vPortISRStartFirstTask( void );

/* Timer tick handler */
long iTickISR( void * Timer );

/*-----------------------------------------------------------*/

/* 
 * Initialise the stack of a task to look exactly as if a call to 
 * portSAVE_CONTEXT had been called.
 *
 * See header file for description. 
 */
portSTACK_TYPE *pxPortInitialiseStack( portSTACK_TYPE *pxTopOfStack, pdTASK_CODE pxCode, void *pvParameters )
{
	portSTACK_TYPE *pxOriginalTOS;

	pxOriginalTOS = pxTopOfStack;

	/* To ensure asserts in tasks.c don't fail, although in this case the assert
	is not really required. */
	pxTopOfStack--;

	/* Setup the initial stack of the task.  The stack is set exactly as
	expected by the portRESTORE_CONTEXT() macro. */

	/* First on the stack is the return address - which in this case is the
	start of the task.  The offset is added to make the return address appear
	as it would within an IRQ ISR. */
	*pxTopOfStack = ( portSTACK_TYPE ) pxCode + portINSTRUCTION_SIZE;
	pxTopOfStack--;

	*pxTopOfStack = ( portSTACK_TYPE ) xPortStartScheduler;	/* R14 */
	pxTopOfStack--;
	/* Stack used when task starts goes in R13. */
	*pxTopOfStack = ( portSTACK_TYPE ) pxOriginalTOS;
	pxTopOfStack--;
	*pxTopOfStack = ( portSTACK_TYPE ) 0x12121212;	/* R12 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x11111111;	/* R11 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x10101010;	/* R10 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x09090909;	/* R9 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x08080808;	/* R8 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x07070707;	/* R7 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x06060606;	/* R6 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x05050505;	/* R5 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x04040404;	/* R4 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x03030303;	/* R3 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x02020202;	/* R2 */
	pxTopOfStack--;	
	*pxTopOfStack = ( portSTACK_TYPE ) 0x01010101;	/* R1 */
	pxTopOfStack--;	

	/* When the task starts is will expect to find the function parameter in
	R0. */
	*pxTopOfStack = ( portSTACK_TYPE ) pvParameters; /* R0 */
	pxTopOfStack--;

	/* The last thing onto the stack is the status register, which is set for
	system mode, with interrupts enabled. */
	*pxTopOfStack = ( portSTACK_TYPE ) portINITIAL_SPSR;

	#ifdef THUMB_INTERWORK
	{
		/* We want the task to start in thumb mode. */
		*pxTopOfStack |= portTHUMB_MODE_BIT;
	}
	#endif

	pxTopOfStack--;

	/* Some optimisation levels use the stack differently to others.  This 
	means the interrupt flags cannot always be stored on the stack and will
	instead be stored in a variable, which is then saved as part of the
	tasks context. */
	*pxTopOfStack = portNO_CRITICAL_SECTION_NESTING;

	return pxTopOfStack;
}
/*-----------------------------------------------------------*/

portBASE_TYPE xPortStartScheduler( void )
{
	if( lSchedStarted != 0 ) {
		/* Delete the task. Passing NULL will delete the current task. */
		vTaskDelete( NULL );
		return 0;
	}

	/* Start the timer that generates the tick ISR.  Interrupts are disabled
	here already. */
	prvSetupTimerInterrupt();

	/* Start the first task. */
	vPortISRStartFirstTask();

	/* Should not get here! */
	return 0;
}
/*-----------------------------------------------------------*/

void vPortEndScheduler( void )
{
	/* It is unlikely that the ARM port will require this function as there
	is nothing to return to.  */
}
/*-----------------------------------------------------------*/

/*
 * Setup the CPU private timer 0 to generate the tick interrupts
 * at the required frequency.
 */
static void prvSetupTimerInterrupt( void )
{
	MV_CNTMR_CTRL	tmctrl;
	long	ticks;

	tmctrl.enable = 1;
	tmctrl.autoEnable = 1;
	tmctrl.Ratio = MV_RATIO_1;
	tmctrl.enable_25Mhz = 0;
	ticks =  ulL2Clk / configTICK_RATE_HZ / 2;
	mvCntmrStart( TIMER5, ticks, &tmctrl );

	iICRegisterHandler( IRQ_LOCAL_TIMER0, iTickISR, ( void * ) TIMER5,
						IRQ_ENABLE, IRQ_PRIO_TICKS );
}
/*-----------------------------------------------------------*/

/*
 * The ISR used for the scheduler ticks.
 */
long iTickISR( void *Timer )
{
	/* Increment the RTOS tick count, then look for the highest priority
	task that is ready to run. */
	vTaskIncrementTick();

	#if configUSE_PREEMPTION == 1
		vTaskSwitchContext();
	#endif

	mvCntmrIntClear( ( long )Timer );

	return 0;
}
/*-----------------------------------------------------------*/

/*
 * Clock detection function
 */
void vGetBoardClocks( void )
{
	unsigned long sar, clk;
	MV_FREQ_MODE freqTable[] = MV_SAR_FREQ_MODES;

	sar = MV_REG_READ( MPP_SAMPLE_AT_RESET( 1 ));
	clk = (( sar & 0x00400000 ) >> 22 ); /* MV_SATR_CORE_CLK_SELECT */
	if( clk == 0 )
		ulTClk = MV_SAR_TCLK_166MHZ;
	else
		ulTClk = MV_SAR_TCLK_200MHZ;

	clk = (( sar & 0x003E0000 ) >> 17 ); /* MV_SATR_CPU_DDR_L2_FREQ */
	if ( clk < ( sizeof( freqTable ) / sizeof( MV_FREQ_MODE ))) {
		ulCpuClk = freqTable[ clk ].cpuFreq * 1000000;
		ulL2Clk = freqTable[ clk ].l2Freq * 1000000;
	}
}
